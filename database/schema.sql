IF OBJECT_ID(N'dbo.BRANCHES', N'U') IS NOT NULL AND COL_LENGTH(N'dbo.BRANCHES', N'AdaptiveSlotCapacityEnabled') IS NULL
ALTER TABLE dbo.BRANCHES ADD AdaptiveSlotCapacityEnabled BIT NOT NULL CONSTRAINT DF_BRANCHES_AdaptiveCap DEFAULT (1);

IF OBJECT_ID(N'dbo.BRANCHES', N'U') IS NOT NULL AND COL_LENGTH(N'dbo.BRANCHES', N'MinSlotTotalCapacity') IS NULL
