/*
  IH-QMS physical tables — names aligned with lecturer ERD / EF Core mappings (QmsDbContext ToTable).
  Bank-style reporting DDL (optional): database/bds_public_layout.sql — same database.
  When deployed, set appsettings "Bds": { "Enabled": true } so the API mirrors ticket lifecycle
  into BDS_QMS_TICKET / BDS_QMS_AUDIT (VALID_DTTM / PROCESSED_DTTM left NULL).

