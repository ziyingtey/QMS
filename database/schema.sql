IF OBJECT_ID(N'dbo.BRANCHES', N'U') IS NULL
CREATE TABLE dbo.BRANCHES (
    Id UNIQUEIDENTIFIER NOT NULL PRIMARY KEY,
    BranchCode INT NOT NULL,
    Name NVARCHAR(200) NOT NULL,
    Address NVARCHAR(400) NOT NULL CONSTRAINT DF_BRANCHES_Address DEFAULT (N''),
    State NVARCHAR(80) NOT NULL CONSTRAINT DF_BRANCHES_State DEFAULT (N''),
    Latitude FLOAT NOT NULL,
    Longitude FLOAT NOT NULL,
    OnlineQuotaPercent INT NOT NULL,
    SlotDurationMinutes INT NOT NULL,
    GeofenceMeters INT NOT NULL,
    ServiceDayStartMinutes INT NOT NULL CONSTRAINT DF_BRANCHES_ServiceDayStart DEFAULT (540),
    ServiceDayEndMinutes INT NOT NULL CONSTRAINT DF_BRANCHES_ServiceDayEnd DEFAULT (1020),
    ServiceZoneOffsetMinutes INT NOT NULL CONSTRAINT DF_BRANCHES_ServiceZoneOffset DEFAULT (480),
    OpeningStatus INT NOT NULL CONSTRAINT DF_BRANCHES_OpeningStatus DEFAULT (0),
    OperatingHours NVARCHAR(200) NULL,
    MaxCapacity INT NULL,
    AdaptiveSlotCapacityEnabled BIT NOT NULL CONSTRAINT DF_BRANCHES_AdaptiveCapNew DEFAULT (1),
    MinSlotTotalCapacity INT NULL
);

IF OBJECT_ID(N'dbo.BRANCHES', N'U') IS NOT NULL AND COL_LENGTH(N'dbo.BRANCHES', N'OpeningStatus') IS NULL
ALTER TABLE dbo.BRANCHES ADD OpeningStatus INT NOT NULL CONSTRAINT DF_BRANCHES_OpeningStatus2 DEFAULT (0);

