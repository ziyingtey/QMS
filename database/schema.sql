IF OBJECT_ID(N'dbo.CUSTOMERS', N'U') IS NULL
CREATE TABLE dbo.CUSTOMERS (
    Id UNIQUEIDENTIFIER NOT NULL PRIMARY KEY,
    Email NVARCHAR(256) NOT NULL,
    Phone NVARCHAR(32) NULL,
    Name NVARCHAR(200) NOT NULL,
    PasswordHash NVARCHAR(500) NOT NULL,
    PreferredBranchId UNIQUEIDENTIFIER NULL,
    CreatedAt DATETIMEOFFSET(7) NOT NULL CONSTRAINT DF_CUSTOMERS_CreatedAt DEFAULT (TODATETIMEOFFSET(SYSUTCDATETIME(), '+00:00')),
    CONSTRAINT FK_CUSTOMERS_BRANCHES_Preferred FOREIGN KEY (PreferredBranchId) REFERENCES dbo.BRANCHES(Id) ON DELETE SET NULL
